#include <stdio.h>

int main() {
    // Khai bao va khoi tao hai bien so nguyen
    int num1 = 15; // Bien so nguyen dau tien, gia tri la 15
    int num2 = 25; // Bien so nguyen thu hai, gia tri la 25

    // Tinh tong va luu ket qua vao bien khac
    int sum = num1 + num2; // Bien sum luu ket qua cua phep cong

    // In ket qua ra man hinh
    printf("Tong cua %d va %d la: %d\n", num1, num2, sum);

    return 0;
}

